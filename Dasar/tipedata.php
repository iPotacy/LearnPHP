<?php 

// Integer

/** Tipe data integer 
 *  digunakan untuk menyimpan bilangan bulat (positif, negatif, atau nol) 
 *  tanpa bagian desimal. */

$data = 25;
var_dump($data); // Outputnya adalah int(25)

// Float

/** Tipe data float digunakan 
 *  untuk menyimpan angka dengan bagian desimal. */

$float = 12.0;
var_dump($float); // Outputnya adalah float(12.0)

// String

/** Tipe data string digunakan 
 *  untuk menyimpan teks atau urutan karakter. */

// Boolean

/** Tipe data boolean digunakan 
 *  untuk menyimpan nilai kebenaran (true atau false). */

$boolean = true;
var_dump($boolean); // Outputnya adalah boolean(true).


?>