<?php

// Fungsi PHP min() dan max()

/** Fungsi min()and max()dapat digunakan 
 *  untuk mencari nilai terendah atau tertinggi dalam daftar argumen: */

 echo (min(10, 11, 4, 2, 20, 40));
 echo (max(10, 11, 4, 2, 20, 40));

// PHP abs() Fungsi

/** Fungsi ini abs()mengembalikan nilai absolut (positif) suatu bilangan: */
 
 echo (abs(-4.10));

// PHP sqrt() Fungsi

/** Fungsi ini sqrt()mengembalikan akar kuadrat suatu bilangan: */

 echo (sqrt(64));

// Putaran PHP() Fungsi

/** Fungsi ini round()membulatkan bilangan floating-point ke bilangan bulat terdekat: */

 echo (round(0.60));
 echo (round(0.49));

// Angka Acak

/** Fungsi ini rand()menghasilkan angka acak: */

 echo(rand(10, 100));

?>